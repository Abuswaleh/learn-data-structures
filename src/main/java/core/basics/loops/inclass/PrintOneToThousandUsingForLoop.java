package core.basics.loops.inclass;

public class PrintOneToThousandUsingForLoop {
    public static void main(String[] args) {
        /**
         * Print numbers between 1 to 1000
         * using for loop.
         */
        for (int i = 1; i <= 1000;  i++){
            System.out.println(i);
        }

    }
}
